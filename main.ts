// This is a gravity block. The gravity is determined by a variable and that variably changed the y position of the player. It also knows when the platform is being touched by the player, and if it was then the gravity will stop.
sprites.onOverlap(SpriteKind.Player, SpriteKind.Platform, function (sprite, otherSprite) {
    gravity = 0
    if (controller.A.isPressed()) {
        gravity = 0
        for (let index = 0; index < 1; index++) {
            gravity += -10
        }
        if (gravity < -10) {
            gravity = 1
        } else if (gravity > 5) {
            gravity = 1
        }
        if (xvel < 2.5) {
            xvel = 0
        } else if (xvel > -2.5) {
            xvel = 0
        }
    }
})
